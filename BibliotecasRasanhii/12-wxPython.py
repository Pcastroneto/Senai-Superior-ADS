'''
wxPython é uma biblioteca que permite a criação de interfaces gráficas para aplicações desktop em Python, utilizando como base a wxWidgets, uma biblioteca em C++. 
A wxPython permite a criação de interfaces modernas e personalizáveis, utilizando uma grande variedade de widgets e componentes. 
A biblioteca possui suporte multiplataforma, sendo possível desenvolver aplicações para Windows, macOS e Linux. 
Um exemplo:


import wx

app = wx.App()
frame = wx.Frame(None, title="Oi, wxPython!", size=(200, 100))
frame.Show()
app.MainLoop()


Ao executar o código, veriamos uma janela com o título "Oi, wxPython!" e o tamanho especificado.
'''